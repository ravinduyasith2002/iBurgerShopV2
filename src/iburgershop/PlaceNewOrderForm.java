    private void txtCustomerIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCustomerIdActionPerformed
        String customerId = txtCustomerId.getText();
        //
        if(customerId.length() != 10 || Character.getNumericValue(customerId.charAt(0))!=0){
            JOptionPane.showMessageDialog(null,"Invalid input !... Enter a valid phone number again..");
            txtCustomerId.requestFocus();
        }else{
            String existCustomerName = BurgerController.checkForCustomerId(customerId);
            if(existCustomerName==null){
                txtCustomerName.setText("");
                txtCustomerName.requestFocus();
            }else{
                txtCustomerName.setText(existCustomerName);
                txtBurgerQuantity.requestFocus();
            }
        }
    }//GEN-LAST:event_txtCustomerIdActionPerformed

