    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        int response = JOptionPane.showConfirmDialog(
                null,
                "Do you want to continue?",
                "Confirm",
                JOptionPane.YES_NO_OPTION);

        if (response == JOptionPane.YES_OPTION) {
                String orderId = txtOrderId.getText();
                String customerName = txtCustomerName.getText();
                String customerId = txtCustomerId.getText();
                int burgerQty = Integer.parseInt(txtBurgerQuantity.getText());
                try{
                    FileWriter fw = new FileWriter("Burger.txt",true);
                    fw.write(orderId+","+customerId+","+customerName+","+burgerQty+","+"1\n");
                    fw.close();
                    JOptionPane.showMessageDialog(null,orderId+" : Order Added Success");
                }catch(IOException ex){
                    ex.printStackTrace();
                }
                
        } else if (response == JOptionPane.NO_OPTION) {
                txtBurgerQuantity.requestFocus();
        } else {
                JOptionPane.showMessageDialog(null,"Error !");
        }
    }//GEN-LAST:event_btnPlaceOrderActionPerformed

