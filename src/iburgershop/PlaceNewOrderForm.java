    private void txtBurgerQuantityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBurgerQuantityActionPerformed
        int orderQuantity = Integer.parseInt(txtBurgerQuantity.getText());
        if(orderQuantity<1){
            JOptionPane.showMessageDialog(null,"Quantity should be positive !");
        }else{
            Burger burger = new Burger();
            lblOrderStatus.setText("Preparing");
            lblOrderTotalValue.setText(Double.toString(orderQuantity*burger.getBURGERPRICE()));
        }
    }//GEN-LAST:event_txtBurgerQuantityActionPerformed

